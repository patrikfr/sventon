<div class="body">
<p>
  sventon is a web client for browsing Subversion repositories. It is powerful, straightforward to use,
  and super easy to install.
  </p>
<p>
sventon is a pure Java application, all you need to run it is a
<a href="http://java.sun.com/j2se/1.5.0/">J2SE 5.0 runtime</a> and a Servlet 2.4/JSP 2.0 compliant webserver,
such as  <a href="http://tomcat.apache.org/">Tomcat 5.5</a>. Installation is super simple, we will have you and
running in no-time!
</p>
<p>
Some of the features making sventon a great Subversion companion:
<ul style="margin-left:40px">
  <li>Browse and download directories and files
  <li>View logs and blame individual files
  <li>Diff your repository files, and their properties</li>
  <li>Support for multiple repositories</li>
  <li>Easily keep track of changes using the 'latest commit' feature</li>
  <li>Lightning fast search for instantly finding files or log messages, including CamelCaseSearch</li>
  <li>Flattening of directory structures for quickly finding the directory you're looking for</li>
  <li>Browse inside archive files</li>
  <li>View thumbnails of image files stored in the repository</li>
  <li>Context sensitive RSS feed support</li>
  <li>Instantly find locked files in the repository</li>
  <li>... and more!</li>
</ul>
<p>Have a look at the <a href="index.php?page=screenshots">screenshots</a> and
<a href="index.php?page=features">feature page</a> for the current feature set.
</p>
<p>
Browse the sventon Subversion repository using <a href="http://svn.sventon.org">sventon</a>.
</p>
