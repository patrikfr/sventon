  <br/>
  <b>Contributing</b>
  <p>
  If you would like to contribute we are happy to accept bug reports, feature requests and patches!
  If you fix bugs and/or build extensions to sventon that you would like to contribute back to the project
  we will be happy to accept such contributions if we find them in line with where we think sventon is going.
  </p>
  <p>
  Please have a look at <a href="http://developer.berlios.de/feature/?group_id=3670">
  http://developer.berlios.de/feature/?group_id=3670</a> for some ideas of features we are
  considering.
  </p>
  <p>
  The latest development sources can be accessed anonymously from
  <code>http://svn.berlios.de/svnroot/repos/sventon/trunk</code>.
  </p>
  <p>
  The best way of communicating would be to use our
  <a href="http://groups.google.com/group/sventon-support/">Google group</a>. Patches can be
  <a href="http://groups.google.com/group/sventon-support/files?upload=1">uploaded to the group</a> or emailed directly
  to <a href="mailto:sventonproject@gmail.com">sventonproject@gmail.com</a>
  </p>

